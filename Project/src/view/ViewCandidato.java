    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            // TODO add your handling code here:
            CandidatoDao dao = new CandidatoDao();
            CandidatoBean bean = listaGlobal.get(indexTabela);
          
            int confirma = JOptionPane.showConfirmDialog(null,"Tem certeda que deseja "
                       + "excluir esse candidato?","Aviso",0,0);
               if(confirma == 0){
                   check = false;
                   txtNome.setText("");
                   dao.excluir(bean.getId());
                   preencherTabela();
               }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        try {
            if(validarCampo()){
               CandidatoDao dao = new CandidatoDao();
               
               if(check){
               CandidatoBean bean1 = listaGlobal.get(indexTabela); 
               int codigo = bean1.getId(); 
               PartidoBean bean2 = listaGlobalSecundaria.get(indexComboBox);
               bean1 = new CandidatoBean(codigo,txtNome.getText(),Integer.parseInt(txtNumero.getText()),bean2);
               dao.alterar(bean1);
               }else{
               PartidoBean bean2 = listaGlobalSecundaria.get(indexComboBox);
               CandidatoBean bean1 = new CandidatoBean(txtNome.getText(),Integer.parseInt(txtNumero.getText()),bean2);    
               dao.cadastrar(bean1);
               }
               preencherTabela();
            }
        } catch (Exception e) {
            System.out.println("Erro ao salvar");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

