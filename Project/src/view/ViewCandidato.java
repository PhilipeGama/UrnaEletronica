    public void preencherTabela(){
      Vector linhas = new Vector(),colunas = new Vector();
        try {
            CandidatoDao dao = new CandidatoDao();
            String valorBuscado = "";
            listaGlobal = dao.consultar(valorBuscado);
            colunas.add("Código");
            colunas.add("Nome");
            colunas.add("Número");
            colunas.add("Partido");
            
            for (CandidatoBean bean : listaGlobal) {
                Vector linha = new Vector();
                linha.add(bean.getId());
                linha.add(bean.getNome());
                linha.add(bean.getNumero());
                linha.add(bean.getPartido().getNome());
                linhas.add(linha);
            }
            DefaultTableModel model = new DefaultTableModel(linhas,colunas);
            tabela.setModel(model);

        } catch (Exception e) {
            System.out.println("Exceção"+e);
        }
    }
    
    public void preecherComboBox(){
        try {
            PartidoDao dao = new PartidoDao();
            listaGlobalSecundaria = dao.consultar("");
            for (PartidoBean bean : listaGlobalSecundaria) {
                cboxPartido.addItem(bean.getNome());
            }
        } catch (Exception e) {
        }
    }
    
    public boolean validarCampo(){
        String nome = txtNome.getText();
        String numero = txtNumero.getText();
        if(nome.isEmpty() || numero.isEmpty()){
            JOptionPane.showMessageDialog(null,"Preenchar todos os campos","Alerta", 0);
        }
        return true;
    }

