    private void carregarTabela(){
        Vector linhas = new Vector(),colunas = new Vector();
        try {
           PartidoDao dao = new PartidoDao();
           //String valorBuscado =
           listaGlobal = dao.consultar("");
           colunas.add("Código");
           colunas.add("Nome");
           
            for (PartidoBean partido : listaGlobal) {
               Vector linha =  new Vector();
              
               linha.add(partido.getId());
               linha.add(partido.getNome());
               linhas.add(linha);
            }
            
            DefaultTableModel model = new DefaultTableModel(linhas,colunas);
            tabela.setModel(model);
            
           
        } catch (Exception e) {
            System.out.println("Exceção: "+e);
        }
        
      
    }
    
    private boolean validarCampo(){
        String nome = txtNome.getText();
        
        if(nome.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Preeenchar todos os campos", "Erro", 0);
            return false;
        }
        return true;  
    }

