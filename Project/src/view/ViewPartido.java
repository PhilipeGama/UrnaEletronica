    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try {
           
           if(check){
               PartidoBean bean = listaGlobal.get(indexTabela);
               PartidoDao dao = new PartidoDao();
               int confirma = JOptionPane.showConfirmDialog(null,"Tem certeda que deseja "
                       + "excluir esse partido?","Aviso",0,0);
               System.out.println("confirma: "+confirma);
               if(confirma == 0){
                   check = false;
                 
                   dao.excluir(bean.getId());
                   txtNome.setText("");
                   carregarTabela();
                  
               }
            
           }else{
               JOptionPane.showMessageDialog(null, "Selecione um partido","Erro",2);
               
           }
               
                   
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        if(validarCampo()){
            try {
                String nome = txtNome.getText();
                PartidoDao dao = new PartidoDao();
                
                if(check){
                    PartidoBean bean = listaGlobal.get(indexTabela);
                    bean.setNome(nome);
                    dao.alterar(bean);
                }else{
                    PartidoBean bean = new PartidoBean();
                    bean.setNome(nome);
                    dao.cadastrar(bean);
                }    
                carregarTabela();
            } catch (Exception ex) {
                System.out.println("Exceção: "+ex);
            }
            
            
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // TODO add your handling code here:
        indexTabela = tabela.getSelectedRow();
        PartidoBean bean = listaGlobal.get(indexTabela);
        txtNome.setText(bean.getNome());
        check = true;
        
    }//GEN-LAST:event_tabelaMouseClicked

