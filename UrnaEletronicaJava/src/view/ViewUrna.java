    private void btnConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaActionPerformed
        // TODO add your handling code here:
        if(confirma == 0){
           
            
            validarCpf();
          
            if(eleitorBean != null){
                
                    int i = JOptionPane.showConfirmDialog(null, "Confirme seu nome é: "+eleitorBean.getNome(),"Confirma",0);
                    if(i == 0){
                        
                    ftxtCpf.setEnabled(false);
                    txtNumero.setEnabled(true);
                    txtNumero.grabFocus();
                    confirma = 1;
                        habilitarBotoes(true, true, true);
                    }
                    
            }else{
                 JOptionPane.showMessageDialog(null, "Cpf não encontrado","Aviso",2);
            }        
           return;     
        }   
        if(confirma == 1){
            validarVoto();
            if(candidatoBean != null){
                 int i = JOptionPane.showConfirmDialog(null, "Confirme seu candidato: "+candidatoBean.getNome(),"Confirma",0);
                    if(i == 0){
                        
                 
                    txtNumero.setEnabled(false);
                    txtCandidato.setText(candidatoBean.getNome());
                    confirma = 2;
                    
                    return ;
                   
            }else{
                 JOptionPane.showMessageDialog(null, "Candidato não encontrado","Aviso",2);
            }
            }
        }
            
         
        if(confirma == 2){
            try {
                int i = JOptionPane.showConfirmDialog(null, "Confirma voto: "+candidatoBean.getNome(),"Confirma",0);
                if(i == 0){
                UrnaBean bean = new UrnaBean(candidatoBean.getNumero(),candidatoBean,eleitorBean);
                UrnaDao dao = new UrnaDao();
                dao.cadastrar(bean);
                ftxtCpf.setText("");
                txtNumero.setText("");
                ftxtCpf.grabFocus();
                ftxtCpf.setEnabled(true);
                confirma = 0;
               
                }else{
                        
                  txtNumero.setEnabled(true);
                  txtNumero.grabFocus();
                  
                }
            } catch (Exception e) {
                System.out.println("Erro: "+e);
                
                
            }
          
        }
                
    }//GEN-LAST:event_btnConfirmaActionPerformed

