    public boolean validarCampos(){
        String cpf = ftxtCpf.getText();
        String numero = txtNumero.getText();
        if(cpf.isEmpty() || numero.isEmpty()){
            JOptionPane.showMessageDialog(null, "Preechar todos os campos","Aviso",0);
            return false;
        }
        
        return true;
    }

